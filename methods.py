def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma(primeiro, segundo):
    soma = primeiro + segundo
    return soma

def subtracao(primeiro, segundo):
    subtracao = primeiro - segundo
    return subtracao

def multiplicacao(primeiro, segundo):
    multiplicacao = primeiro * segundo
    return multiplicacao

def divisao(primeiro, segundo):
    divisao = primeiro / segundo
    return divisao